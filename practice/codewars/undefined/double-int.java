class Java {
  public static int doubleInteger(int i) {
    // Double the integer and return it!
    return i * 2;
  }
  public static void main(String[] args) {
    int original = 5; // Replace with the integer you want to double
    int doubled = doubleInteger(original);
    System.out.println("Original value: " + original);
    System.out.println("Doubled value: " + doubled);
  }
}